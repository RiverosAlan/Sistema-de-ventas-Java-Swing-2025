    public void ingresar() {

        KeyboardFocusManager.getCurrentKeyboardFocusManager().addKeyEventDispatcher(new KeyEventDispatcher() {
            @Override
            public boolean dispatchKeyEvent(KeyEvent e) {
                if (e.getID() == KeyEvent.KEY_PRESSED && e.getKeyCode() == KeyEvent.VK_ENTER) {
                    loginUsuario();
                }
                return false; // Permite que otros componentes sigan recibiendo eventos
            }
        });

    }

    public void loginUsuario() {

        //obteniendo datos del formulario
        String email = txtUsername.getText();
        String password = new String(txtPassword.getPassword());

        Usuario usuario = new UsuarioControlador().login(email, password);

        if (usuario != null) {

            // 1 si el usuario es Administrador
            if (usuario.getIdRol() == 1) {

                VistaAdministrador vista1 = new VistaAdministrador(usuario);

                //cerramos la vista del Login
                setVisible(false);

                //Abrimos la vista 1
                vista1.setVisible(true);
            }

            if (usuario.getIdRol() == 2) {

                VistaVendedor vista2 = new VistaVendedor(usuario);
                //cerramos la vista del Login
                setVisible(false);

                //Abrimos la vista 1
                vista2.setVisible(true);
            }

        } else {
            JOptionPane.showMessageDialog(null, "Credenciales incorrectas");
        }

    }

